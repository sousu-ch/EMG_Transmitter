// CRC8テーブルから計算
unsigned char GetCRC8_TABLE(unsigned char *buff, unsigned char size)
{
    unsigned char *data = (unsigned char *)buff;
    unsigned char crc8 = CRC8INITVALUE;

    while (size != 0)
    {
#ifdef __AVR_ARCH__
        crc8 = pgm_read_byte(&CRC8_TABLE[crc8 ^ *data]);
#else
        crc8 = CRC8_TABLE[crc8 ^ *data];
#endif
        data++;
        size--;
    }
    return crc8;
}

// CRC8の計算
unsigned char GetCRC8(unsigned char *buff, unsigned char size)
{
    unsigned char *p = (unsigned char *)buff;
    unsigned char crc8;
    int i;

    for (crc8 = CRC8INITVALUE; size != 0; size--)
    { //crc8=00の部分が初期値。初期値変更する場合はここを変える
        crc8 ^= *p++;
        for (i = 0; i < 8; i++)
        { //CHAR_BIT=8として置き変え
            if (crc8 & 0x80)
            {
                crc8 <<= 1;
                crc8 ^= CRC8POLY;
            }
            else
            {
                crc8 <<= 1;
            }
        }
    }
    return crc8;
}
